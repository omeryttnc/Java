package UDEMY.denemlerim;

/*
// Program to find the sum of natural numbers from 1 to 1000.

 */
public class loop_exercise_till_1000 {
    public static void main(String[] args) {

        int count = 0;
        for (int i = 0; i < 5; i++) {
            count += i;}
            System.out.println("sum for " + ": " + count);

    }
}
