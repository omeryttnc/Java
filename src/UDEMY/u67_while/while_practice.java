package UDEMY.u67_while;

public class while_practice {
    public static void main(String[] args) {
        int count = 1;
        while (count != 6) {
            System.out.println("count " + count);
            count++;
        }

        for (int i = 1; i < 7; i++) {
            System.out.println("count for " + i);
            count++;
        }

    }
}

