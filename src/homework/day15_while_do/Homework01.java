package homework.day15_while_do;
/*
1) Write a program to print numbers from 1 to 5 on the console by using do-while loop.

 */
public class Homework01 {
    public static void main(String[] args) {
        int i = 1;

        do{
            System.out.println(i);
            i++;
        }while(i<=5);
    }
}
