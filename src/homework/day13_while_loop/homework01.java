package homework.day13_while_loop;
/*
1) Type java code by using while loop,
Write a program to print numbers from 1 to 5.


 */
public class homework01 {
    public static void main(String[] args) {
        int i = 1;
        while(i<=5){
            System.out.println(i);
            i++;
        }
    }
}
