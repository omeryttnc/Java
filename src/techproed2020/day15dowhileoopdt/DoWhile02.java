package techproed2020.day15dowhileoopdt;

public class DoWhile02 {
    public static void main(String[] args) {
        /*
        Print first 10 counting numbers on the console by using do-while loop
         */

        int i =1;
        do{
            System.out.print(i +" ");
            i++;
        }while(i<=10);
    }
}
