package techproed2020.day03scannerIncrement;

public class Decrement01 {
    public static void main(String[] args) {
        // Decrement to decrease te value of a variable

        int num = 18;
        num = num -3;

        num-=2;

        num--;
        System.out.println(num);

        num = num * 2;
        System.out.println(num);

        num*=2;
        System.out.println(num);


        num = num/6;
        System.out.println(num);

    }
}
